const fs = require('fs');

// 更新 artifacts API - 支持 multipart 文件上传和自动解析
const L = [];
const p = (s) => L.push(s);

p("import { NextResponse } from 'next/server'");
p("import { prisma } from '@/lib/prisma'");
p("import { requireAuth } from '@/lib/session'");
p("import { parseFile, parseZip } from '@/lib/file-parser'");
p("");
p("export const runtime = 'nodejs'");
p("");
p("export async function POST(");
p("  request: Request,");
p("  { params }: { params: Promise<{ id: string; modelId: string }> }");
p(") {");
p("  const session = await requireAuth()");
p("  if (!session) return NextResponse.json({ error: '未登录' }, { status: 401 })");
p("  const { id, modelId } = await params");
p("");
p("  const model = await prisma.taskModel.findFirst({");
p("    where: { id: modelId, task: { userId: session.userId, id } },");
p("  })");
p("  if (!model) return NextResponse.json({ error: '模型不存在' }, { status: 404 })");
p("");
p("  const contentType = request.headers.get('content-type') || ''");
p("");
p("  // JSON body - 文本粘贴方式（兼容旧版）");
p("  if (contentType.includes('application/json')) {");
p("    const { name, url, textContent, mimeType, size, parsedText } = await request.json()");
p("    const artifact = await prisma.modelArtifact.create({");
p("      data: {");
p("        taskModelId: modelId,");
p("        name: name || 'untitled',");
p("        url: url || '',");
p("        textContent: textContent || null,");
p("        mimeType: mimeType || null,");
p("        size: size || null,");
p("        parsedText: parsedText || textContent || null,");
p("      },");
p("    })");
p("    return NextResponse.json({ artifact })");
p("  }");
p("");
p("  // Multipart form - 文件上传方式");
p("  if (!contentType.includes('multipart/form-data')) {");
p("    return NextResponse.json({ error: '不支持的 Content-Type' }, { status: 400 })");
p("  }");
p("");
p("  const formData = await request.formData()");
p("  const files = formData.getAll('files') as File[]");
p("");
p("  if (files.length === 0) {");
p("    return NextResponse.json({ error: '请上传文件' }, { status: 400 })");
p("  }");
p("");
p("  const created = []");
p("");
p("  for (const file of files) {");
p("    const buffer = Buffer.from(await file.arrayBuffer())");
p("    let parsedText = ''");
p("");
p("    try {");
p("      // ZIP 文件特殊处理：解压后解析所有文本文件");
p("      if (file.name.toLowerCase().endsWith('.zip') || file.type === 'application/zip') {");
p("        const zipResult = await parseZip(buffer)");
p("        parsedText = zipResult.files.map((f) => `=== ${f.name} ===\\n${f.text}`).join('\\n\\n');");
p("      } else {");
p("        parsedText = await parseFile(buffer, file.name, file.type)");
p("      }");
p("    } catch (e: any) {");
p("      console.error('文件解析失败:', file.name, e.message)");
p("      parsedText = '[文件解析失败: ' + (e.message || '未知错误') + ']'");
p("    }");
p("");
p("    const artifact = await prisma.modelArtifact.create({");
p("      data: {");
p("        taskModelId: modelId,");
p("        name: file.name,");
p("        url: '', // 本地部署暂不存储原始文件，只存解析后的文本");
p("        mimeType: file.type || null,");
p("        size: file.size,");
p("        textContent: '', // 原始文本内容（如果是纯文本）");
p("        parsedText: parsedText || '[无法解析该文件格式]',");
p("      },");
p("    })");
p("    created.push(artifact)");
p("  }");
p("");
p("  return NextResponse.json({ artifacts: created })");
p("}");
p("");
p("export async function DELETE(");
p("  request: Request,");
p("  { params }: { params: Promise<{ id: string; modelId: string }> }");
p(") {");
p("  const session = await requireAuth()");
p("  if (!session) return NextResponse.json({ error: '未登录' }, { status: 401 })");
p("  const { modelId } = await params");
p("  const body = await request.json()");
p("  const artifactId = body.artifactId");
p("");
p("  const artifact = await prisma.modelArtifact.findFirst({");
p("    where: { id: artifactId, taskModel: { id: modelId, task: { userId: session.userId } } },");
p("  })");
p("  if (!artifact) return NextResponse.json({ error: '文件不存在' }, { status: 404 })");
p("");
p("  await prisma.modelArtifact.delete({ where: { id: artifactId } })");
p("  return NextResponse.json({ ok: true })");
p("}");

const content = L.join('\n');
fs.writeFileSync('src/app/api/tasks/[id]/models/[modelId]/artifacts/route.ts', content);
console.log('artifacts route rewritten, lines:', L.length);
