const fs = require('fs');

const L = [];
const p = (s) => L.push(s);

p('"use client";');
p('');
p('import { useState, useEffect, useRef } from "react";');
p('import { useParams, useRouter } from "next/navigation";');
p('import Link from "next/link";');
p('import StepInfo from "./StepInfo";');
p('import StepIdea from "./StepIdea";');
p('import StepScreenshot from "./StepScreenshot";');
p('import StepArtifact from "./StepArtifact";');
p('import StepReport from "./StepReport";');
p('');
p('const steps = [');
p('  { key: "INFO", label: "任务信息", desc: "填写测试任务基本信息" },');
p('  { key: "IDEA", label: "测试思路", desc: "AI 生成测试思路" },');
p('  { key: "SCREENSHOT", label: "截图分析", desc: "上传执行过程截图 & 数据看板" },');
p('  { key: "ARTIFACT", label: "产物分析", desc: "上传各模型产物进行分析" },');
p('  { key: "REPORT", label: "评估报告", desc: "生成最终评估报告" },');
p('];');
p('');
p('export default function TaskPage() {');
p('  const params = useParams();');
p('  const router = useRouter();');
p('  const taskId = params.id as string;');
p('');
p('  const [task, setTask] = useState<any>(null);');
p('  const [loading, setLoading] = useState(true);');
p('  const [currentStep, setCurrentStep] = useState("INFO");');
p('  const [messages, setMessages] = useState<any[]>([]);');
p('  const [chatInput, setChatInput] = useState("");');
p('  const [sending, setSending] = useState(false);');
p('  const chatEndRef = useRef<HTMLDivElement>(null);');
p('');
p('  async function loadTask() {');
p('    setLoading(true);');
p('    const res = await fetch("/api/tasks/" + taskId);');
p('    const data = await res.json();');
p('    if (data.task) {');
p('      setTask(data.task);');
p('      setCurrentStep(data.task.currentStep || "INFO");');
p('    }');
p('    setLoading(false);');
p('  }');
p('');
p('  async function loadMessages() {');
p('    const res = await fetch("/api/tasks/" + taskId + "/messages");');
p('    const data = await res.json();');
p('    if (data.messages) {');
p('      setMessages(data.messages);');
p('    }');
p('  }');
p('');
p('  useEffect(() => {');
p('    loadTask();');
p('    loadMessages();');
p('  }, [taskId]);');
p('');
p('  useEffect(() => {');
p('    chatEndRef.current?.scrollIntoView({ behavior: "smooth" });');
p('  }, [messages, currentStep]);');
p('');
p('  const stepMessages = messages.filter((m) => m.step === currentStep);');
p('');
p('  function handleTaskUpdate(updated: any) {');
p('    setTask(updated);');
p('    if (updated.currentStep) {');
p('      setCurrentStep(updated.currentStep);');
p('    }');
p('  }');
p('');
p('  function handleAddMessage(msg: any) {');
p('    setMessages((prev) => [...prev, msg]);');
p('  }');
p('');
p('  function goToStep(stepKey: string) {');
p('    setCurrentStep(stepKey);');
p('    fetch("/api/tasks/" + taskId, {');
p('      method: "PUT",');
p('      headers: { "Content-Type": "application/json" },');
p('      body: JSON.stringify({ currentStep: stepKey }),');
p('    }).then((r) => r.json()).then((data) => {');
p('      if (data.task) setTask(data.task);');
p('    });');
p('  }');
p('');
p('  async function handleChatSend(e: React.FormEvent) {');
p('    e.preventDefault();');
p('    if (!chatInput.trim() || sending) return;');
p('');
p('    const userMsg = {');
p('      id: "user-" + Date.now(),');
p('      role: "user",');
p('      content: chatInput.trim(),');
p('      step: currentStep,');
p('    };');
p('    setMessages((prev) => [...prev, userMsg]);');
p('    setChatInput("");');
p('    setSending(true);');
p('');
p('    try {');
p('      const res = await fetch("/api/tasks/" + taskId + "/chat", {');
p('        method: "POST",');
p('        headers: { "Content-Type": "application/json" },');
p('        body: JSON.stringify({');
p('          message: userMsg.content,');
p('          step: currentStep,');
p('        }),');
p('      });');
p('      const data = await res.json();');
p('      if (data.assistantMessage) {');
p('        setMessages((prev) => [...prev, data.assistantMessage]);');
p('      } else if (data.error) {');
p('        setMessages((prev) => [');
p('          ...prev,');
p('          { id: "err-" + Date.now(), role: "system", content: "错误: " + data.error, step: currentStep },');
p('        ]);');
p('      }');
p('    } catch (err: any) {');
p('      setMessages((prev) => [');
p('        ...prev,');
p('        { id: "err-" + Date.now(), role: "system", content: "网络错误: " + err.message, step: currentStep },');
p('      ]);');
p('    } finally {');
p('      setSending(false);');
p('    }');
p('  }');
p('');
p('  function renderStepContent() {');
p('    if (!task) return null;');
p('    switch (currentStep) {');
p('      case "INFO":');
p('        return <StepInfo task={task} onUpdate={handleTaskUpdate} />;');
p('      case "IDEA":');
p('        return <StepIdea task={task} onAddMessage={handleAddMessage} />;');
p('      case "SCREENSHOT":');
p('        return <StepScreenshot task={task} onAddMessage={handleAddMessage} onRefresh={loadTask} />;');
p('      case "ARTIFACT":');
p('        return <StepArtifact task={task} onAddMessage={handleAddMessage} onRefresh={loadTask} />;');
p('      case "REPORT":');
p('        return <StepReport task={task} onAddMessage={handleAddMessage} onRefresh={loadTask} />;');
p('      default:');
p('        return null;');
p('    }');
p('  }');
p('');
p('  if (loading) {');
p('    return (');
p('      <div className="text-center py-20 text-slate-400">加载中...</div>');
p('    );');
p('  }');
p('');
p('  if (!task) {');
p('    return (');
p('      <div className="text-center py-20">');
p('        <p className="text-slate-500 mb-4">任务不存在或已被删除</p>');
p('        <Link href="/dashboard" className="text-blue-600 hover:underline text-sm">返回任务列表</Link>');
p('      </div>');
p('    );');
p('  }');
p('');
p('  return (');
p('    <div className="space-y-6">');
p('      <div className="flex items-center justify-between">');
p('        <div className="flex items-center gap-4">');
p('          <Link href="/dashboard" className="text-slate-400 hover:text-slate-600 text-sm">');
p('            ← 返回');
p('          </Link>');
p('          <div>');
p('            <h1 className="text-xl font-semibold text-slate-900">{task.title}</h1>');
p('            <p className="text-sm text-slate-500 mt-0.5">');
p('              共 {task.models?.length || 0} 个待测模型 · 创建于 ');
p('              {new Date(task.createdAt).toLocaleDateString("zh-CN")}');
p('            </p>');
p('          </div>');
p('        </div>');
p('        <div className="flex items-center gap-2">');
p('          <a href={"/api/tasks/" + taskId + "/export"} className="px-3 py-1.5 text-sm border border-slate-300 rounded-lg hover:bg-slate-50 transition">');
p('            导出');
p('          </a>');
p('          <button onClick={async () => {');
p('              if (!confirm("确定删除此任务？此操作不可撤销。")) return;');
p('              await fetch("/api/tasks/" + taskId, { method: "DELETE" });');
p('              router.push("/dashboard");');
p('            }} className="px-3 py-1.5 text-sm text-red-600 border border-red-200 rounded-lg hover:bg-red-50 transition">');
p('            删除');
p('          </button>');
p('        </div>');
p('      </div>');
p('');
p('      <div className="bg-white rounded-xl border border-slate-200 p-2">');
p('        <div className="flex gap-1">');
p('          {steps.map((s, idx) => {');
p('            const isActive = currentStep === s.key;');
p('            return (');
p('              <button key={s.key} onClick={() => goToStep(s.key)}');
p('                className={"flex-1 px-3 py-3 rounded-lg text-left transition " + (isActive ? "bg-blue-50 border border-blue-200" : "hover:bg-slate-50 border border-transparent")}>');
p('                <div className="flex items-center gap-2">');
p('                  <span className={"w-6 h-6 rounded-full text-xs font-medium flex items-center justify-center " + (isActive ? "bg-blue-600 text-white" : "bg-slate-200 text-slate-600")}>');
p('                    {idx + 1}');
p('                  </span>');
p('                  <span className={"font-medium text-sm " + (isActive ? "text-blue-700" : "text-slate-700")}>');
p('                    {s.label}');
p('                  </span>');
p('                </div>');
p('                <p className="text-xs text-slate-500 mt-1 ml-8">{s.desc}</p>');
p('              </button>');
p('            );');
p('          })}');
p('        </div>');
p('      </div>');
p('');
p('      <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">');
p('        <div className="lg:col-span-2">');
p('          {renderStepContent()}');
p('        </div>');
p('');
p('        <div className="bg-white rounded-xl border border-slate-200 flex flex-col h-[600px]">');
p('          <div className="px-4 py-3 border-b border-slate-200">');
p('            <h3 className="font-medium text-sm text-slate-800">AI 助手</h3>');
p('            <p className="text-xs text-slate-500 mt-0.5">');
p('              当前步骤：{steps.find((s) => s.key === currentStep)?.label}');
p('            </p>');
p('          </div>');
p('');
p('          <div className="flex-1 overflow-y-auto p-4 space-y-3">');
p('            {stepMessages.length === 0 ? (');
p('              <div className="text-center text-slate-400 text-sm py-8">');
p('                开始和 AI 讨论当前步骤的内容吧');
p('              </div>');
p('            ) : (');
p('              stepMessages.map((msg) => (');
p('                <div key={msg.id} className={"text-sm " + (msg.role === "user" ? "text-right" : msg.role === "system" ? "text-center" : "")}>');
p('                  {msg.role === "system" ? (');
p('                    <span className="text-xs text-red-500 bg-red-50 px-2 py-1 rounded">{msg.content}</span>');
p('                  ) : (');
p('                    <div className={"inline-block max-w-[85%] px-3 py-2 rounded-lg " + (msg.role === "user" ? "bg-blue-600 text-white" : "bg-slate-100 text-slate-800")}>');
p('                      <div className="whitespace-pre-wrap leading-relaxed">{msg.content}</div>');
p('                    </div>');
p('                  )}');
p('                </div>');
p('              ))');
p('            )}');
p('            <div ref={chatEndRef} />');
p('          </div>');
p('');
p('          <form onSubmit={handleChatSend} className="p-3 border-t border-slate-200">');
p('            <div className="flex gap-2">');
p('              <input name="chatInput" type="text" value={chatInput}');
p('                onChange={(e) => setChatInput(e.target.value)}');
p('                placeholder={sending ? "AI 思考中..." : "输入消息..."}');
p('                disabled={sending}');
p('                className="flex-1 px-3 py-2 border border-slate-300 rounded-lg text-sm focus:outline-none focus:ring-2 focus:ring-blue-500 disabled:opacity-50"');
p('              />');
p('              <button type="submit" disabled={sending || !chatInput.trim()}');
p('                className="px-4 py-2 bg-blue-600 hover:bg-blue-700 text-white text-sm rounded-lg font-medium transition disabled:opacity-50">');
p('                {sending ? "..." : "发送"}');
p('              </button>');
p('            </div>');
p('          </form>');
p('        </div>');
p('      </div>');
p('    </div>');
p('  );');
p('}');

const content = L.join('\n');
fs.writeFileSync('src/app/tasks/[id]/page.tsx', content);
console.log('page.tsx written, lines:', L.length, 'chars:', content.length);
