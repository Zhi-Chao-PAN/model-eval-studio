const fs = require('fs');

// 生成 StepScreenshot 组件 - 支持 base64 图片上传
const L = [];
const p = (s) => L.push(s);

p("'use client'");
p('');
p('import { useState } from "react"');
p('');
p('interface Props {');
p('  task: any');
p('  onAddMessage: (msg: any) => void');
p('  onRefresh: () => void');
p('}');
p('');
p('export default function StepScreenshot({ task, onAddMessage, onRefresh }: Props) {');
p('  const [activeTab, setActiveTab] = useState<"process" | "dashboard">("process")');
p('  const [processImages, setProcessImages] = useState<{ name: string; dataUrl: string }[]>([])');
p('  const [dashboardImages, setDashboardImages] = useState<{ name: string; dataUrl: string }[]>([])');
p('  const [analyzing, setAnalyzing] = useState(false)');
p('  const [result, setResult] = useState<any>(null)');
p('  const [error, setError] = useState<string | null>(null)');
p('');
p('  function fileToDataUrl(file: File): Promise<string> {');
p('    return new Promise((resolve, reject) => {');
p('      const reader = new FileReader()');
p('      reader.onload = () => resolve(reader.result as string)');
p('      reader.onerror = reject');
p('      reader.readAsDataURL(file)');
p('    })');
p('  }');
p('');
p('  async function handleFile(e: React.ChangeEvent<HTMLInputElement>, type: "process" | "dashboard") {');
p('    const files = Array.from(e.target.files || [])');
p('    if (files.length === 0) return');
p('    setError(null)');
p('');
p('    const images = await Promise.all(files.map(async (f) => ({');
p('      name: f.name,');
p('      dataUrl: await fileToDataUrl(f),');
p('    })))');
p('');
p('    if (type === "process") {');
p('      setProcessImages((prev) => [...prev, ...images])');
p('    } else {');
p('      setDashboardImages((prev) => [...prev, ...images])');
p('    }');
p('    // reset input');
p('    e.target.value = ""');
p('  }');
p('');
p('  async function analyze() {');
p('    const images = activeTab === "process" ? processImages : dashboardImages');
p('    if (images.length === 0) {');
p('      alert("请先上传图片")');
p('      return');
p('    }');
p('');
p('    setAnalyzing(true)');
p('    setResult(null)');
p('    setError(null)');
p('    try {');
p('      const res = await fetch("/api/tasks/" + task.id + "/analyze-screenshots", {');
p('        method: "POST",');
p('        headers: { "Content-Type": "application/json" },');
p('        body: JSON.stringify({');
p('          images: images.map((i) => i.dataUrl),');
p('          type: activeTab,');
p('        }),');
p('      })');
p('      const data = await res.json()');
p('      if (!res.ok) {');
p('        setError(data.error || "分析失败")');
p('        return');
p('      }');
p('      setResult(data)');
p('      onAddMessage({ id: "a-" + Date.now(), role: "assistant", content: data.raw || "分析完成", step: "SCREENSHOT" })');
p('      onRefresh()');
p('    } catch (e: any) {');
p('      setError("网络错误: " + e.message)');
p('    } finally {');
p('      setAnalyzing(false)');
p('    }');
p('  }');
p('');
p('  function removeImage(type: "process" | "dashboard", index: number) {');
p('    if (type === "process") {');
p('      setProcessImages((prev) => prev.filter((_, i) => i !== index))');
p('    } else {');
p('      setDashboardImages((prev) => prev.filter((_, i) => i !== index))');
p('    }');
p('  }');
p('');
p('  const models = task.models || []');
p('  const currentImages = activeTab === "process" ? processImages : dashboardImages');
p('');
p('  return (');
p('    <div className="bg-white rounded-xl border border-slate-200 p-6 space-y-5">');
p('      <div className="flex items-center justify-between">');
p('        <h2 className="text-lg font-semibold">步骤 3：截图分析</h2>');
p('      </div>');
p('');
p('      <div className="flex gap-1 border-b border-slate-200">');
p('        <button');
p('          onClick={() => setActiveTab("process")}');
p('          className={"px-4 py-2 text-sm font-medium border-b-2 -mb-px " + (activeTab === "process" ? "border-blue-600 text-blue-600" : "border-transparent text-slate-500 hover:text-slate-900")}');
p('        >');
p('          执行过程截图');
p('        </button>');
p('        <button');
p('          onClick={() => setActiveTab("dashboard")}');
p('          className={"px-4 py-2 text-sm font-medium border-b-2 -mb-px " + (activeTab === "dashboard" ? "border-blue-600 text-blue-600" : "border-transparent text-slate-500 hover:text-slate-900")}');
p('        >');
p('          数据看板截图');
p('        </button>');
p('      </div>');
p('');
p('      <p className="text-sm text-slate-500">');
p('        {activeTab === "process"');
p('          ? "上传待测模型的任务执行过程截图，AI 会分析各模型的执行轨迹和问题点。"');
p('          : "上传模型能力评估看板截图（如工具调用次数、耗时等数据），AI 会自动识别各模型代号和硬指标。"}');
p('      </p>');
p('');
p('      <div className="space-y-3">');
p('        <label className="flex items-center justify-center w-full h-24 border-2 border-dashed border-slate-300 rounded-lg cursor-pointer hover:border-blue-400 hover:bg-blue-50 transition">');
p('          <input type="file" accept="image/*" multiple className="hidden" onChange={(e) => handleFile(e, activeTab)} />');
p('          <span className="text-slate-500 text-sm">+ 上传{activeTab === "process" ? "过程" : "看板"}截图</span>');
p('        </label>');
p('');
p('        {currentImages.length > 0 && (');
p('          <div className="grid grid-cols-3 gap-2">');
p('            {currentImages.map((img, i) => (');
p('              <div key={i} className="relative aspect-video bg-slate-100 rounded overflow-hidden border border-slate-200 group">');
p('                <img src={img.dataUrl} alt={img.name} className="w-full h-full object-cover" />');
p('                <button');
p('                  onClick={() => removeImage(activeTab, i)}');
p('                  className="absolute top-1 right-1 w-5 h-5 bg-black/50 text-white rounded-full text-xs opacity-0 group-hover:opacity-100 transition"');
p('                >');
p('                  ×');
p('                </button>');
p('              </div>');
p('            ))}');
p('          </div>');
p('        )}');
p('      </div>');
p('');
p('      {error && (');
p('        <div className="bg-red-50 border border-red-200 rounded-lg p-3 text-sm text-red-600">{error}</div>');
p('      )}');
p('');
p('      <div className="flex items-center gap-3">');
p('        <button');
p('          onClick={analyze}');
p('          disabled={analyzing || currentImages.length === 0}');
p('          className="bg-blue-600 hover:bg-blue-700 text-white px-4 py-2 rounded-lg text-sm font-medium transition disabled:opacity-50"');
p('        >');
p('          {analyzing ? "AI 分析中..." : "开始 AI 分析"}');
p('        </button>');
p('        <span className="text-xs text-slate-400">分析后将自动识别模型并提取指标</span>');
p('      </div>');
p('');
p('      {result && (');
p('        <div className="bg-slate-50 rounded-lg p-4 border border-slate-200">');
p('          <p className="text-sm font-medium text-slate-700 mb-2">');
p('            分析结果：{result.parsed ? "识别到 " + (result.parsed.models?.length || 0) + " 个模型" : "已完成（未识别到结构化数据）"}');
p('          </p>');
p('          {result.parsed?.models && (');
p('            <div className="space-y-2">');
p('              {result.parsed.models.map((m: any, i: number) => (');
p('                <div key={i} className="text-sm bg-white p-2 rounded border border-slate-200">');
p('                  <span className="font-medium">{m.modelCode || m.displayName || "未知模型"}</span>');
p('                  {m.status && <span className="text-slate-500 ml-2">状态：{m.status}</span>}');
p('                  {m.toolCalls !== undefined && <span className="text-slate-500 ml-2">工具调用 {m.toolCalls} 次</span>}');
p('                </div>');
p('              ))}');
p('            </div>');
p('          )}');
p('        </div>');
p('      )}');
p('');
p('      {models.length > 0 && (');
p('        <div>');
p('          <h3 className="text-sm font-medium text-slate-700 mb-2">已识别的模型</h3>');
p('          <div className="flex flex-wrap gap-2">');
p('            {models.map((m: any) => (');
p('              <span key={m.id} className="px-3 py-1.5 bg-blue-50 text-blue-600 text-sm rounded-full">');
p('                {m.modelCode}');
p('              </span>');
p('            ))}');
p('          </div>');
p('        </div>');
p('      )}');
p('    </div>');
p('  );');
p('}');

const content = L.join('\n');
fs.writeFileSync('src/app/tasks/[id]/StepScreenshot.tsx', content);
console.log('StepScreenshot.tsx rewritten, lines:', L.length);
