const fs = require('fs');

// 更新 analyze-screenshots API - 接收 base64 图片数据
const L = [];
const p = (s) => L.push(s);

p("import { NextResponse } from 'next/server'");
p("import { prisma } from '@/lib/prisma'");
p("import { requireAuth } from '@/lib/session'");
p("import { getUserAiConfig } from '@/lib/user-ai'");
p("import { analyzeImages } from '@/lib/ai'");
p("import { buildScreenshotAnalysisPrompt, buildDashboardAnalysisPrompt } from '@/lib/ai-prompts'");
p("");
p("export async function POST(");
p("  request: Request,");
p("  { params }: { params: Promise<{ id: string }> }");
p(") {");
p("  const session = await requireAuth()");
p("  if (!session) return NextResponse.json({ error: '未登录' }, { status: 401 })");
p("  const { id } = await params");
p("  const { images, type } = await request.json()");
p("");
p("  if (!Array.isArray(images) || images.length === 0) {");
p("    return NextResponse.json({ error: '请上传图片' }, { status: 400 })");
p("  }");
p("");
p("  const task = await prisma.task.findFirst({");
p("    where: { id, userId: session.userId },");
p("    include: { models: true },");
p("  })");
p("  if (!task) return NextResponse.json({ error: '任务不存在' }, { status: 404 })");
p("");
p("  const aiConfig = await getUserAiConfig(session.userId)");
p("  if (!aiConfig) {");
p("    return NextResponse.json({ error: '请先配置 AI API' }, { status: 400 })");
p("  }");
p("");
p("  if (aiConfig.provider !== 'OPENAI_COMPAT') {");
p("    return NextResponse.json(");
p("      { error: '截图分析需要 OpenAI 兼容接口的多模态模型，请切换 provider' },");
p("      { status: 400 }");
p("    )");
p("  }");
p("");
p("  const prompt = type === 'dashboard'");
p("    ? buildDashboardAnalysisPrompt()");
p("    : buildScreenshotAnalysisPrompt()");
p("");
p("  const result = await analyzeImages(images, prompt, {");
p("    baseUrl: aiConfig.baseUrl,");
p("    apiKey: aiConfig.apiKey,");
p("    model: aiConfig.model,");
p("    provider: aiConfig.provider as any,");
p("    temperature: 0.3,");
p("    maxTokens: 4000,");
p("  })");
p("");
p("  // 尝试解析 JSON");
p("  let parsed: any = null");
p("  try {");
p("    // 去掉 ```json 包裹");
p("    const clean = result.replace(/```json\\n?/g, '').replace(/```/g, '').trim()");
p("    parsed = JSON.parse(clean)");
p("  } catch {");
p("    // 解析失败就返回原始文本");
p("    return NextResponse.json({ raw: result, parsed: null })");
p("  }");
p("");
p("  // 如果是过程截图且识别到了模型，创建/更新 model 记录");
p("  if (type !== 'dashboard' && parsed.models) {");
p("    for (const m of parsed.models) {");
p("      const existing = task.models.find((tm) => tm.modelCode === m.modelCode)");
p("      if (existing) {");
p("        await prisma.taskModel.update({");
p("          where: { id: existing.id },");
p("          data: {");
p("            processText: m.processDetail || m.processSummary,");
p("            screenshotUrls: images.length + ' images',");
p("          },");
p("        })");
p("      } else {");
p("        await prisma.taskModel.create({");
p("          data: {");
p("            taskId: id,");
p("            modelCode: m.modelCode,");
p("            displayName: m.displayName || m.modelCode,");
p("            processText: m.processDetail || m.processSummary,");
p("            screenshotUrls: images.length + ' images',");
p("          },");
p("        })");
p("      }");
p("    }");
p("  }");
p("");
p("  // 如果是看板截图，更新硬指标数据");
p("  if (type === 'dashboard' && parsed.models) {");
p("    for (const m of parsed.models) {");
p("      const existing = task.models.find((tm) => tm.modelCode === m.modelCode)");
p("      if (existing) {");
p("        await prisma.taskModel.update({");
p("          where: { id: existing.id },");
p("          data: { hardMetricsJson: JSON.stringify(m.metrics || m) },");
p("        })");
p("      } else {");
p("        // 看板截图识别到新模型也创建");
p("        await prisma.taskModel.create({");
p("          data: {");
p("            taskId: id,");
p("            modelCode: m.modelCode,");
p("            displayName: m.displayName || m.modelCode,");
p("            hardMetricsJson: JSON.stringify(m.metrics || m),");
p("          },");
p("        })");
p("      }");
p("    }");
p("  }");
p("");
p("  await prisma.task.update({");
p("    where: { id },");
p("    data: { currentStep: 'SCREENSHOT' },");
p("  })");
p("");
p("  return NextResponse.json({ parsed, raw: result })");
p("}");

const content = L.join('\n');
fs.writeFileSync('src/app/api/tasks/[id]/analyze-screenshots/route.ts', content);
console.log('analyze-screenshots route rewritten, lines:', L.length);
