const fs = require('fs');

const L = [];
const p = (s) => L.push(s);

p("'use client'");
p('');
p('import { useState, useEffect, useRef } from "react"');
p('');
p('interface Props {');
p('  task: any');
p('  onAddMessage: (msg: any) => void');
p('  onRefresh: () => void');
p('}');
p('');
p('export default function StepArtifact({ task, onAddMessage, onRefresh }: Props) {');
p('  const [analysis, setAnalysis] = useState("")');
p('  const [analyzing, setAnalyzing] = useState(false)');
p('  const [selectedModel, setSelectedModel] = useState<string | null>(null)');
p('  const [textContent, setTextContent] = useState("")');
p('  const [uploading, setUploading] = useState(false)');
p('  const fileInputRef = useRef<HTMLInputElement>(null)');
p('');
p('  const models = task.models || []');
p('');
p('  useEffect(() => {');
p('    if (task.analysisJson) {');
p('      try {');
p('        const parsed = JSON.parse(task.analysisJson)');
p('        setAnalysis(parsed.content || "")');
p('      } catch {}');
p('    }');
p('  }, [task.analysisJson])');
p('');
p('  async function analyze() {');
p('    if (models.length === 0) {');
p('      alert("还没有模型，请先在步骤 3 上传截图识别模型，或手动添加模型")');
p('      return');
p('    }');
p('    setAnalyzing(true)');
p('    try {');
p('      const res = await fetch("/api/tasks/" + task.id + "/analyze-artifacts", { method: "POST" })');
p('      const data = await res.json()');
p('      if (data.analysis) {');
p('        setAnalysis(data.analysis)');
p('        onAddMessage({ id: "a-" + Date.now(), role: "assistant", content: data.analysis, step: "ARTIFACT" })');
p('      } else if (data.error) {');
p('        alert(data.error)');
p('      }');
p('    } finally {');
p('      setAnalyzing(false)');
p('    }');
p('  }');
p('');
p('  async function handleFileUpload(e: React.ChangeEvent<HTMLInputElement>, modelId: string) {');
p('    const files = Array.from(e.target.files || [])');
p('    if (files.length === 0) return');
p('');
p('    setUploading(true)');
p('    try {');
p('      const formData = new FormData()');
p('      files.forEach((f) => formData.append("files", f))');
p('');
p('      const res = await fetch("/api/tasks/" + task.id + "/models/" + modelId + "/artifacts", {');
p('        method: "POST",');
p('        body: formData,');
p('      })');
p('      if (res.ok) {');
p('        onRefresh()');
p('      } else {');
p('        const data = await res.json()');
p('        alert("上传失败: " + (data.error || "未知错误"))');
p('      }');
p('    } finally {');
p('      setUploading(false)');
p('      if (fileInputRef.current) fileInputRef.current.value = ""');
p('    }');
p('  }');
p('');
p('  async function addTextArtifact() {');
p('    if (!selectedModel || !textContent.trim()) return');
p('    const res = await fetch("/api/tasks/" + task.id + "/models/" + selectedModel + "/artifacts", {');
p('      method: "POST",');
p('      headers: { "Content-Type": "application/json" },');
p('      body: JSON.stringify({ name: "文本内容.txt", textContent }),');
p('    })');
p('    if (res.ok) {');
p('      setTextContent("")');
p('      setSelectedModel(null)');
p('      onRefresh()');
p('    }');
p('  }');
p('');
p('  async function deleteArtifact(modelId: string, artifactId: string) {');
p('    if (!confirm("确定删除此文件？")) return');
p('    await fetch("/api/tasks/" + task.id + "/models/" + modelId + "/artifacts", {');
p('      method: "DELETE",');
p('      headers: { "Content-Type": "application/json" },');
p('      body: JSON.stringify({ artifactId }),');
p('    })');
p('    onRefresh()');
p('  }');
p('');
p('  return (');
p('    <div className="space-y-5">');
p('      {models.length === 0 ? (');
p('        <div className="bg-white rounded-xl border border-slate-200 p-8 text-center text-slate-400 text-sm">');
p('          暂无模型，请先在步骤 3 上传数据看板截图，AI 会自动识别待测模型。');
p('        </div>');
p('      ) : (');
p('        <div className="grid gap-4">');
p('          {models.map((m: any) => (');
p('            <div key={m.id} className="bg-white rounded-xl border border-slate-200 p-4">');
p('              <div className="flex items-center justify-between mb-3">');
p('                <h3 className="font-medium text-slate-900">{m.modelCode}</h3>');
p('                <span className="text-xs text-slate-400">');
p('                  {m.artifacts?.length || 0} 个文件');
p('                </span>');
p('              </div>');
p('');
p('              {m.artifacts?.length > 0 && (');
p('                <div className="space-y-1 mb-3">');
p('                  {m.artifacts.map((a: any) => (');
p('                    <div key={a.id} className="flex items-center justify-between text-sm text-slate-600 bg-slate-50 px-3 py-1.5 rounded group">');
p('                      <span>{a.name}</span>');
p('                      <button');
p('                        onClick={() => deleteArtifact(m.id, a.id)}');
p('                        className="text-red-400 hover:text-red-600 opacity-0 group-hover:opacity-100 transition text-xs"');
p('                      >');
p('                        删除');
p('                      </button>');
p('                    </div>');
p('                  ))}');
p('                </div>');
p('              )}');
p('');
p('              <div className="flex gap-3">');
p('                <button');
p('                  onClick={() => setSelectedModel(m.id)}');
p('                  className="text-xs text-blue-600 hover:underline"');
p('                >');
p('                  + 粘贴文本');
p('                </button>');
p('                <label className="text-xs text-blue-600 hover:underline cursor-pointer">');
p('                  {uploading ? "上传中..." : "+ 上传文件"}');
p('                  <input');
p('                    ref={fileInputRef}');
p('                    type="file"');
p('                    className="hidden"');
p('                    multiple');
p('                    accept=".pdf,.docx,.xlsx,.pptx,.txt,.md,.csv,.json,.zip"');
p('                    onChange={(e) => handleFileUpload(e, m.id)}');
p('                    disabled={uploading}');
p('                  />');
p('                </label>');
p('              </div>');
p('              <p className="text-xs text-slate-400 mt-2">');
p('                支持 PDF / Word / Excel / PPT / 文本 / ZIP 压缩包，自动解析内容');
p('              </p>');
p('            </div>');
p('          ))}');
p('        </div>');
p('      )}');
p('');
p('      {selectedModel && (');
p('        <div className="fixed inset-0 bg-black/50 flex items-center justify-center z-50">');
p('          <div className="bg-white rounded-xl p-6 w-full max-w-lg">');
p('            <h3 className="font-medium mb-3">粘贴产物文本</h3>');
p('            <textarea');
p('              value={textContent}');
p('              onChange={(e) => setTextContent(e.target.value)}');
p('              rows={12}');
p('              className="w-full px-3 py-2 border border-slate-300 rounded-lg text-sm font-mono focus:outline-none focus:ring-2 focus:ring-blue-500"');
p('              placeholder="将模型输出的产物文本粘贴到这里..."');
p('              autoFocus');
p('            />');
p('            <div className="flex justify-end gap-2 mt-4">');
p('              <button');
p('                onClick={() => setSelectedModel(null)}');
p('                className="px-4 py-2 border border-slate-300 rounded-lg text-sm hover:bg-slate-50"');
p('              >');
p('                取消');
p('              </button>');
p('              <button');
p('                onClick={addTextArtifact}');
p('                disabled={!textContent.trim()}');
p('                className="px-4 py-2 bg-blue-600 text-white rounded-lg text-sm hover:bg-blue-700 disabled:opacity-50"');
p('              >');
p('                添加');
p('              </button>');
p('            </div>');
p('          </div>');
p('        </div>');
p('      )}');
p('');
p('      <div className="bg-white rounded-xl border border-slate-200 p-6 space-y-4">');
p('        <div className="flex items-center justify-between">');
p('          <h3 className="font-medium">整体分析</h3>');
p('          <button');
p('            onClick={analyze}');
p('            disabled={analyzing || models.length === 0}');
p('            className="bg-blue-600 hover:bg-blue-700 text-white px-4 py-2 rounded-lg text-sm font-medium transition disabled:opacity-50"');
p('          >');
p('            {analyzing ? "AI 分析中..." : "开始 AI 分析"}');
p('          </button>');
p('        </div>');
p('');
p('        {analysis ? (');
p('          <div className="bg-slate-50 rounded-lg p-4 text-sm whitespace-pre-wrap font-mono text-slate-800 leading-relaxed border border-slate-200 max-h-96 overflow-y-auto">');
p('            {analysis}');
p('          </div>');
p('        ) : (');
p('          <p className="text-sm text-slate-400 text-center py-8">');
p('            上传各模型产物后，点击上方按钮进行 AI 整体对比分析');
p('          </p>');
p('        )}');
p('      </div>');
p('    </div>');
p('  );');
p('}');

const content = L.join('\n');
fs.writeFileSync('src/app/tasks/[id]/StepArtifact.tsx', content);
console.log('StepArtifact.tsx rewritten, lines:', L.length);
