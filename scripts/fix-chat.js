const fs = require('fs');

// ===== 1. 重写 chat/route.ts =====
const lines = [];
lines.push("import { NextResponse } from 'next/server'");
lines.push("import { prisma } from '@/lib/prisma'");
lines.push("import { requireAuth } from '@/lib/session'");
lines.push("import { getUserAiConfig } from '@/lib/user-ai'");
lines.push("import { generateChat } from '@/lib/ai'");
lines.push("import { buildSystemPrompt } from '@/lib/ai-prompts'");
lines.push("");
lines.push("export async function POST(");
lines.push("  request: Request,");
lines.push("  { params }: { params: Promise<{ id: string }> }");
lines.push(") {");
lines.push("  const session = await requireAuth()");
lines.push("  if (!session) return NextResponse.json({ error: '未登录' }, { status: 401 })");
lines.push("  const { id } = await params");
lines.push("  const { message, step, modelId } = await request.json()");
lines.push("");
lines.push("  if (!message || !step) {");
lines.push("    return NextResponse.json({ error: 'message 和 step 必填' }, { status: 400 })");
lines.push("  }");
lines.push("");
lines.push("  const task = await prisma.task.findFirst({");
lines.push("    where: { id, userId: session.userId },");
lines.push("    include: {");
lines.push("      models: { include: { artifacts: true, reports: { take: 1, orderBy: { createdAt: 'desc' } } } },");
lines.push("      attachments: true,");
lines.push("    },");
lines.push("  })");
lines.push("  if (!task) return NextResponse.json({ error: '任务不存在' }, { status: 404 })");
lines.push("");
lines.push("  const aiConfig = await getUserAiConfig(session.userId)");
lines.push("  if (!aiConfig) {");
lines.push("    return NextResponse.json({ error: '请先配置 AI API' }, { status: 400 })");
lines.push("  }");
lines.push("");
lines.push("  // 历史消息");
lines.push("  const history = await prisma.taskMessage.findMany({");
lines.push("    where: { taskId: id, step },");
lines.push("    orderBy: { createdAt: 'desc' },");
lines.push("    take: 20,");
lines.push("  })");
lines.push("  history.reverse()");
lines.push("");
lines.push("  // 构建任务上下文");
lines.push("  let context = `当前任务：${task.title}");
lines.push("当前步骤：${step}");
lines.push("任务说明：${task.description || '无'}");
lines.push("用户背景：${task.backgroundUsed || aiConfig.background}`");
lines.push("");
lines.push("  if (task.models.length > 0) {");
lines.push("    context += `\\n\\n待测模型：`");
lines.push("    for (const m of task.models) {");
lines.push("      context += `\\n- ${m.modelCode}`");
lines.push("      if (m.displayName && m.displayName !== m.modelCode) {");
lines.push("        context += `（${m.displayName}）`");
lines.push("      }");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  if (modelId) {");
lines.push("    const m = task.models.find((x) => x.id === modelId)");
lines.push("    if (m) {");
lines.push("      context += `\\n\\n当前讨论模型：${m.modelCode}`");
lines.push("      if (m.reports?.[0]) {");
lines.push("        context += `，综合评分：${m.reports[0].overallScore}`");
lines.push("      }");
lines.push("    }");
lines.push("  }");
lines.push("");
lines.push("  const messages = [");
lines.push("    { role: 'system', content: buildSystemPrompt(task.backgroundUsed || aiConfig.background) + `\\n\\n任务上下文：\\n` + context },");
lines.push("    ...history.map((m) => ({ role: m.role, content: m.content })),");
lines.push("    { role: 'user', content: message },");
lines.push("  ]");
lines.push("");
lines.push("  const reply = await generateChat(messages, {");
lines.push("    baseUrl: aiConfig.baseUrl,");
lines.push("    apiKey: aiConfig.apiKey,");
lines.push("    model: aiConfig.model,");
lines.push("    provider: aiConfig.provider,");
lines.push("    temperature: 0.7,");
lines.push("    maxTokens: 2000,");
lines.push("  })");
lines.push("");
lines.push("  const [userMsg, assistantMsg] = await Promise.all([");
lines.push("    prisma.taskMessage.create({");
lines.push("      data: { taskId: id, role: 'user', content: message, step, modelId },");
lines.push("    }),");
lines.push("    prisma.taskMessage.create({");
lines.push("      data: { taskId: id, role: 'assistant', content: reply, step, modelId },");
lines.push("    }),");
lines.push("  ])");
lines.push("");
lines.push("  return NextResponse.json({");
lines.push("    userMessage: userMsg,");
lines.push("    assistantMessage: assistantMsg,");
lines.push("    reply,");
lines.push("  })");
lines.push("}");

fs.writeFileSync('src/app/api/tasks/[id]/chat/route.ts', lines.join('\n'));
console.log('chat route.ts rewritten, lines:', lines.length);
